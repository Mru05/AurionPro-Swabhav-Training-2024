package Homework;

public class BitwiseOperator {

	public static void main(String[] args) {
		        int a = 5;
		        int b = 7;
		        System.out.println("This is bitwise operator code here :");
		        System.out.println("a= "+a+ " b= "+b);

		        // bitwise and
		        // 0101 & 0111=0101 = 5
		        System.out.println("a&b = " + (a & b));

		        // bitwise or
		        // 0101 | 0111=0111 = 7
		        System.out.println("a|b = " + (a | b));

		        // bitwise not
		        System.out.println("~a = " + ~a);

		     
		    }
		


	}


